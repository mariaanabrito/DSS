    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jLabel8.setVisible(false);
        jLabel9.setVisible(false);
        String nome = jTextField4.getText();
        String email = jTextField2.getText();
        String numTel = jTextField3.getText();
        String pass = jTextField1.getText();
        String convite = jTextField5.getText();
        Inquilino i, atual;
        atual = façade.getSessao();
        int aux = atual.getNumCC();
        Conta c = atual.getConta();
        int telemovel;
        try
        {
            telemovel = Integer.parseInt(numTel);
            if (convite.equals(""))
            {
                i = new Inquilino(aux, email, telemovel, nome, pass, c);
                façade.atualizaInquilino(i);
                jLabel9.setVisible(true);
            }
            else if (convite.equals(façade.getConvite()))
                {
                i = new Administrador(aux, email, telemovel, nome, pass, c);
                façade.atualizaInquilino(i);
                jLabel9.setVisible(true);
                
                }
                else
                {
                    jLabel8.setVisible(true);
                    jTextField4.setText(atual.getNome());
                    jTextField2.setText(atual.getEmail());
                    jTextField3.setText(atual.getNumTelemovel()+"");
                    jTextField1.setText(atual.getPassword());
                    jTextField5.setText("");
                }
        }
        catch(NumberFormatException e)
        {
            jLabel8.setVisible(true);
            jTextField4.setText(atual.getNome());
            jTextField2.setText(atual.getEmail());
            jTextField3.setText(atual.getNumTelemovel()+"");
            jTextField1.setText(atual.getPassword());
            jTextField5.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


