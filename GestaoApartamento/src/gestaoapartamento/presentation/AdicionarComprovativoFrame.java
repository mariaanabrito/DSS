    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jLabel3.setVisible(false);
        jLabel8.setVisible(false);
        String value = jComboBox1.getSelectedItem().toString();
        String[] componentes = value.split(" - ");
        String dia = jTextField2.getText();
        String mes = jTextField3.getText();
        String ano = jTextField4.getText();
        
        try
        {
            if(Integer.parseInt(dia) < 0 || Integer.parseInt(dia) > 31
               || Integer.parseInt(mes) < 0 || Integer.parseInt(mes) > 12
               || Integer.parseInt(ano) < 0)
            {
                jLabel3.setVisible(true);
                jTextField2.setText("");
                jTextField3.setText("");
                jTextField4.setText("");
            }
            else
            {
                String data = dia + "-" + mes + "-" + ano;
                façade.insereComprovativo(Integer.parseInt(componentes[0]), data);
                jLabel8.setVisible(true);
            }
                
        }
        catch(NumberFormatException e)
        {
            jLabel3.setVisible(true);
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
        }
     
    }//GEN-LAST:event_jButton1ActionPerformed

